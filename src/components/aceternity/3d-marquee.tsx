'use client';

import { motion } from 'motion/react';
import type { CSSProperties } from 'react';
import { useMemo, useSyncExternalStore } from 'react';

import { cn } from '@/lib/utils';

type ThreeDMarqueeProps = {
  images: MarqueeImage[];
  className?: string;
};

export type MarqueeImage = {
  src: string;
  srcSet?: string;
  sizes?: string;
  alt?: string;
};

type GridLineProps = {
  className?: string;
  offset?: string;
};

function useIsMobileViewport(): boolean {
  return useSyncExternalStore(
    (callback) => {
      const mediaQuery = window.matchMedia('(max-width: 767px)');
      mediaQuery.addEventListener('change', callback);
      return () => mediaQuery.removeEventListener('change', callback);
    },
    () => window.matchMedia('(max-width: 767px)').matches,
    () => true,
  );
}

export const ThreeDMarquee = ({ images, className }: ThreeDMarqueeProps) => {
  const isMobileViewport = useIsMobileViewport();
  const mobileSizes = '(max-width: 767px) 45vw, 180px';
  const desktopSizes = '(max-width: 1024px) 280px, 406px';
  const defaultSizes = isMobileViewport ? mobileSizes : desktopSizes;

  const mobileMarqueeImages = useMemo(() => {
    const mobileUniqueLimit = 4;
    const repeatCount = 6;
    const uniqueImages = images
      .filter(
        (image, index, allImages) =>
          allImages.findIndex((candidate) => candidate.src === image.src) ===
          index,
      )
      .slice(0, mobileUniqueLimit);

    return Array.from({ length: repeatCount }, () => uniqueImages).flat();
  }, [images]);

  const desktopMarqueeImages = useMemo(() => images.slice(0, 36), [images]);
  const activeImages = isMobileViewport
    ? mobileMarqueeImages
    : desktopMarqueeImages;

  const chunkSize = Math.ceil(activeImages.length / 4);
  const chunks = Array.from({ length: 4 }, (_, colIndex) => {
    const start = colIndex * chunkSize;
    return activeImages.slice(start, start + chunkSize);
  });

  return (
    <div
      className={cn(
        'mx-auto block h-[600px] overflow-hidden rounded-2xl max-md:h-[300px]',
        className,
      )}
    >
      <div className="flex size-full items-center justify-center">
        <div className="size-[1320px] shrink-0 scale-[0.56] md:size-[1720px] md:scale-75 lg:scale-100">
          <div
            style={{
              transform: 'rotateX(55deg) rotateY(0deg) rotateZ(-45deg)',
            }}
            className="relative top-[34.25rem] left-[48%] grid size-full origin-top-left -translate-x-1/2 grid-cols-4 gap-8 transform-3d max-md:-ml-[500px] md:top-96 md:right-[50%] md:left-auto md:translate-x-0"
          >
            {chunks.map((subarray, colIndex) => (
              <motion.div
                animate={{ y: colIndex % 2 === 0 ? 100 : -100 }}
                transition={{
                  duration: colIndex % 2 === 0 ? 10 : 15,
                  repeat: Infinity,
                  repeatType: 'reverse',
                }}
                key={`${colIndex}-marquee`}
                className="flex flex-col items-start gap-8"
              >
                <GridLineVertical className="-left-4" offset="80px" />
                {subarray.map((image, imageIndex) => (
                  <div className="relative" key={`${imageIndex}-${image.src}`}>
                    <GridLineHorizontal className="-top-4" offset="20px" />
                    <motion.img
                      whileHover={{ y: -10 }}
                      transition={{
                        duration: 0.3,
                        ease: 'easeInOut',
                      }}
                      src={image.src}
                      srcSet={image.srcSet}
                      sizes={image.sizes ?? defaultSizes}
                      alt={image.alt ?? `Image ${imageIndex + 1}`}
                      loading={imageIndex === 0 ? 'eager' : 'lazy'}
                      decoding="async"
                      fetchPriority="low"
                      className="aspect-[970/700] rounded-lg object-cover ring ring-gray-950/5 hover:shadow-2xl"
                      width={970}
                      height={700}
                    />
                  </div>
                ))}
              </motion.div>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
};

const GridLineHorizontal = ({ className, offset }: GridLineProps) => {
  return (
    <div
      style={
        {
          '--background': '#ffffff',
          '--color': 'rgba(0, 0, 0, 0.2)',
          '--height': '1px',
          '--width': '5px',
          '--fade-stop': '90%',
          '--offset': offset || '200px',
          '--color-dark': 'rgba(255, 255, 255, 0.2)',
          maskComposite: 'exclude',
        } as CSSProperties
      }
      className={cn(
        'absolute left-[calc(var(--offset)/2*-1)] h-[var(--height)] w-[calc(100%+var(--offset))]',
        'bg-[linear-gradient(to_right,var(--color),var(--color)_50%,transparent_0,transparent)]',
        '[background-size:var(--width)_var(--height)]',
        '[mask:linear-gradient(to_left,var(--background)_var(--fade-stop),transparent),_linear-gradient(to_right,var(--background)_var(--fade-stop),transparent),_linear-gradient(black,black)]',
        '[mask-composite:exclude]',
        'z-30',
        'dark:bg-[linear-gradient(to_right,var(--color-dark),var(--color-dark)_50%,transparent_0,transparent)]',
        className,
      )}
    />
  );
};

const GridLineVertical = ({ className, offset }: GridLineProps) => {
  return (
    <div
      style={
        {
          '--background': '#ffffff',
          '--color': 'rgba(0, 0, 0, 0.2)',
          '--height': '5px',
          '--width': '1px',
          '--fade-stop': '90%',
          '--offset': offset || '150px',
          '--color-dark': 'rgba(255, 255, 255, 0.2)',
          maskComposite: 'exclude',
        } as CSSProperties
      }
      className={cn(
        'absolute top-[calc(var(--offset)/2*-1)] h-[calc(100%+var(--offset))] w-[var(--width)]',
        'bg-[linear-gradient(to_bottom,var(--color),var(--color)_50%,transparent_0,transparent)]',
        '[background-size:var(--width)_var(--height)]',
        '[mask:linear-gradient(to_top,var(--background)_var(--fade-stop),transparent),_linear-gradient(to_bottom,var(--background)_var(--fade-stop),transparent),_linear-gradient(black,black)]',
        '[mask-composite:exclude]',
        'z-30',
        'dark:bg-[linear-gradient(to_bottom,var(--color-dark),var(--color-dark)_50%,transparent_0,transparent)]',
        className,
      )}
    />
  );
};
